<details>
	<summary>What is a snowflake?</summary>
	<p>
		A Snowflake is essentially a unique ID for a resource which contains a
		timestamp.
	</p>
	<p>You can get the snowflake from a url of tweet, dm, etc.</p>
	<p>This will let you check the age of a Twitter account and a tweet.</p>
	<p>
		A snowflake of a tweet will be appear on the detailed tweet url after
		'status'. If you have a url
		'https://twitter.com/Twitter/status/1396969503708389379', the snowflake is
		'1396969503708389379'.
	</p>
	<p>
		A snowflake of a user will be appear also on the url of dm page. If you have
		a url 'https://twitter.com/messages/7080152-783214', the snowflake is
		'7080152' and '783214'.
	</p>
	<p>
		The either of them is your snowflake and the other is the snowflake of user
		you are talking to.
	</p>
	<p>
		You can get a user snowflake also by using some tool published on the
		internet.
	</p>
</details>

